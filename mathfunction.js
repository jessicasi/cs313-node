function solveMath(operand1, operation, operand2) {
    if (operation = "+") {
        var result = operand1 + operand2;
        return result;
    }
    if (operation = "-") {
        var result = operand1 - operand2;
        return result;
    }
    if (operation = "*") {
        var result = operand1 * operand2;
        return result;
    }
    if (operation = "/") {
        var result = operand1 / operand2;
        return result;
    }
}